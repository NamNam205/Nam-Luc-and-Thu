package com.mycompany.app.W3School;
import java.until.Scanner;
public class Eifelse {
    public static void main(String[] args) {
        int a = sc.nextInt();
        int b = sc.nextInt();
        Scanner sc = new Scanner(System.in);
        if (a == b) {
            System.out.println("a = b");
        }
        else if (a > b) {
            System.out.println("a > b");
        }
        else {
            System.out.println("a < b");
        }
    }
}
